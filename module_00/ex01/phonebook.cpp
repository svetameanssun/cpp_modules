void PhoneBook::initialize(Color thm){
	ind = 0;
	count_n = 0;
    theme = thm;
}

void PhoneBook::addContact() {

    if((count_n) >= maxCont) {
        if(ind == maxCont) {
            std::cout << "You reached the maximum number of contacts!\n The oldest contact will be rewritten\n";
            ind = 0;
            try {
                contacts[ind].createContact(ind, theme);
            } catch(const EmptyInputException& err) {
                std::cerr << theme.getRed() << "Error: " << err.what() << theme.getReset() << std::endl;
                ind--;
            }   
        } else {
            std::cout << "The oldest contact will be rewritten\n";
            try {
                contacts[ind].createContact(ind, theme);
            } catch(const EmptyInputException& err) {
                std::cerr << theme.getRed() << "Error: " << err.what() << theme.getReset() << std::endl;
                ind--;
            }
        }
    } else {
        try {
            contacts[ind].createContact(ind, theme);
            
        } catch(const EmptyInputException& err) {
            std::cerr << theme.getRed() << "Error: " << err.what() << theme.getReset() << std::endl;
            count_n--;
            ind--;
        }
    }
    count_n++;
    ind++;
}

int PhoneBook::check_index(std::string input_str){
    
    const char * input_c_str;
    input_c_str = input_str.c_str();
    int i = 0;
    long int input_long;
    
    if (input_str.empty())
        return (-1);
    while(input_c_str[i])
    {
        if (!isdigit(input_c_str[i])){
            return (-1);
        }
        i++;
    }
	input_long = atol(input_c_str);
    if (input_long < 0 || input_long >= maxCont)
    {
        return (-1);
    }
    return ((int)input_long);
}

void PhoneBook::displayPhoneBook() {
    int i = 0;
    std::string input_str;
    std::cout << "\n"<< theme.getWarm() << "|"
          << std::setw(10) << "Index" << "|"
          << std::setw(10) << "First Name" << "|"
          << std::setw(10) << "Last Name" << "|"
          << std::setw(10) << "Nickname" << "|"
          << std::endl;
    std::cout << std::string(45, '-') << theme.getReset() << std::endl;
    int res = (count_n < maxCont) ? count_n : maxCont;
	while(i < res) {
        contacts[i].showShortContact();
        i++;
    }
	std::cout << theme.getCalm() <<"Choose the index to see the contact\n" << theme.getReset();
    getline(std::cin, input_str);
    res = check_index(input_str);
	if (res == -1)
    {
        std::cout << theme.getRed() << "The wrong input! Introduce a digit that matches the indexes" << theme.getRed() << std::endl;
        displayPhoneBook();
        return;   
    }
	contacts[res].showLongContact();
}

